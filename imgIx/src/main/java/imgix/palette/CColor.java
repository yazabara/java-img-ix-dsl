public class CColor {
    
    private Float red;
    private Float green;
    private Float blue;

    public CColor() {
    }

    public Float getRed() {
        return red;
    }

    public void setRed(Float red) {
        this.red = red;
    }

    public Float getGreen() {
        return green;
    }

    public void setGreen(Float green) {
        this.green = green;
    }

    public Float getBlue() {
        return blue;
    }

    public void setBlue(Float blue) {
        this.blue = blue;
    }
}
